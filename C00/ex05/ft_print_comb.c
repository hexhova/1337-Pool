#include <unistd.h>

void	ft_print_comb(void)
{
	char	d3;
	char	d2;
	char	d1;

	d3 = '0';
	while (d3 <= '7')
	{
		d2 = d3 + 1;
		while (d2 <= '8')
		{
			d1 = d2 + 1;
			while (d1 <= '9')
			{	
				write(1, &d3, 1);
				write(1, &d2, 1);
				write(1, &d1, 1);
				if (d3 != '7')
					write(1, ", ", 2);
				d1++;
			}
			d2++;
		}
		d3++;
	}
}
