#include <unistd.h>

void	ft_print(char ch)
{
	write (1, &ch, 1);
}

void	ft_print_comb2(void)
{
	int	p1;
	int	p2;

	p1 = 0;
	p2 = 0;
	while (p1 <= 98)
	{
		p2 = p1 + 1;
		while (p2 <= 99)
		{
			ft_print((p1 / 10) + '0');
			ft_print((p1 % 10) + '0');
			write(1, " ", 1);
			ft_print((p2 / 10) + '0');
			ft_print((p2 % 10) + '0');
			if (p1 < 98 || p2 < 99)
				write(1, ", ", 2);
			p2++;
		}
		p1++;
	}
}
