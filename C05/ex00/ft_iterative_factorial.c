int	ft_iterative_factorial(int nb)
{
	int	i;
	int	f;

	i = nb;
	f = 1;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	while (i > 1)
	{
		f *= i;
		i--;
	}
	return (f);
}
