#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;
	
	i = 0;
	j = 0;
	while (--ac) 
	{
		j = 0;
		while (av[ac][j])
		{
			write(1, &av[ac][j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
}
