char	*ft_strcapitalize(char *str)
{	
	int	i;
	int	p;

	i = 0;
	p = 0;
	if (str[0] >= 97 && str[0] <= 122)
		str[0] -= 32;
	i++;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] += 32;
		if (str[i] >= 97 && str[i] <= 122)
		{
			p = i - 1;
			if (!((str[p] >= 97 && str[p] <= 122)
					|| (str[p] >= 65 && str[p] <= 90)
					|| (str[p] >= 48 && str[p] <= 57)))
				str[i] -= 32;
		}
		i++;
	}
	return (str);
}
