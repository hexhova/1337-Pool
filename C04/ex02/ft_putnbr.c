#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	lnb;
	char	c;

	lnb = nb;
	if (nb < 0)
	{
		lnb *= -1;
		write(1, "-", 1);
	}
	if (lnb / 10)
		ft_putnbr(lnb / 10);
	c = (lnb % 10) + '0';
	write(1, &c, 1);
}
int main()
{
	return 0;
}
